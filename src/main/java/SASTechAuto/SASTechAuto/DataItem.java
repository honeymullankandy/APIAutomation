package SASTechAuto.SASTechAuto;

public class DataItem {
    private int id;
    private int value;

    // Getter and Setter for 'id'
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    // Getter and Setter for 'value'
    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }
}